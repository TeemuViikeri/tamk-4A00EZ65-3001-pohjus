// let variable
let 🐈 = "cats are the best!"
// let defines a constant variable so new values can't be assigned: cats truly are best animals
// 🐈 = "cats are not the best!" <- commented out, would throw an error

print(🐈)

// var variable
var age = 0
// var defines a "normal" variable, new values can be assigned: age can change through the years
age = 1
age = 2
age = 3

print(age)